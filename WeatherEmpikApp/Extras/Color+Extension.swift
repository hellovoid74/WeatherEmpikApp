import UIKit

extension UIColor {
    
    @nonobjc class var violet: UIColor {
        return UIColor (red: 175/255, green: 156/255, blue: 249/255, alpha: 0.6)
    }
    
    @nonobjc class var cobalt: UIColor {
        return UIColor(red: 0/255, green: 71/255, blue: 171/255, alpha: 0.6)
    }
    
    @nonobjc class var cobalt2: UIColor {
        return UIColor(red: 40/255, green: 88/255, blue: 156/255, alpha: 1)
    }
}
